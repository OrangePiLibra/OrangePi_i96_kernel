static int NT35510S_mcu_init_gpio(void)
{
	gpio_request(GPIO_LCD_RESET, "lcd reset");
	gpio_direction_output(GPIO_LCD_RESET, 1);
	gpio_set_value(GPIO_LCD_RESET, 1);
	mdelay(10);
	gpio_set_value(GPIO_LCD_RESET, 0);
	mdelay(20);
	gpio_set_value(GPIO_LCD_RESET, 1);
	mdelay(100);

	return 0;
}

static void NT35510S_mcu_readid(void)
{
	u8 data[6];

	/* read id */
	LCD_NT35510S_MCU_CMD(0x400);
	LCD_NT35510S_MCU_READ(&data[0]);
	LCD_NT35510S_MCU_READ(&data[1]);
	LCD_NT35510S_MCU_CMD(0x401);
	LCD_NT35510S_MCU_READ(&data[2]);
	LCD_NT35510S_MCU_READ(&data[3]);
	LCD_NT35510S_MCU_CMD(0x402);
	LCD_NT35510S_MCU_READ(&data[4]);
	LCD_NT35510S_MCU_READ(&data[5]);

	printk(KERN_INFO "rda_fb: NT35510S_mcu ID:"
	       "%02x %02x %02x %02x %02x %02x\n",
	       data[0], data[1], data[2], data[3], data[4], data[5]);
}

